package COREJAVAPROGRAM;

public class LadderIfElse {
    public static void main(String[] args) {
        int num=-20;
        if(num>0){
            System.out.println("Positive");
        }
        else if(num<0){
            System.out.println("Negative");
        }
        else{
            System.out.println("Zero");
        }
    }
    
}
