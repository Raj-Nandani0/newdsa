package COREJAVAPROGRAM;

public class Switchcase {
    public static void main(String[] args) {
        int num=10;
        switch(num){
            case  10:
            System.out.println(num);
            break;
            case 20:
            System.out.println(num);
            break;
            case 30:
            System.out.println(num);
            break;
            default:
            System.out.println("invalid num");
        }
    }
    
}
